/**
 * Generate Multi-Tenancy RLS Migration
 *
 * Queries the database schema to discover all tables, categorizes them,
 * and outputs backend/migrations/124_multi_tenancy_rls.sql
 *
 * Usage: node backend/scripts/generate-tenant-migration.js
 */

const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '..', '.env') });
const { rawPool: pool } = require('../db');
const fs = require('fs');

const OLD_TENANT_UUID = '3a519217-b409-4b53-967f-703a25e70ac7';
const NEW_TENANT_UUID = 'a0000000-0000-0000-0000-000000000000';

// Tables that already have UUID tenant_id with FK to tenants(id) - skip adding column
const SKIP_TABLES = new Set([
  'tenants',
  'skulytics_import_matches',
  'tenant_product_overrides',
]);

// Marketplace tables with INTEGER tenant_id referencing marketplace_tenants
const MARKETPLACE_RENAME_TABLES = new Set([
  'marketplace_channels',
  'marketplace_listings',
  'marketplace_orders',
  'marketplace_inventory_sync',
]);

async function generate() {
  try {
    // 1. Get all base tables in public schema
    const tablesResult = await pool.query(`
      SELECT table_name
      FROM information_schema.tables
      WHERE table_schema = 'public'
        AND table_type = 'BASE TABLE'
      ORDER BY table_name
    `);
    const allTables = tablesResult.rows.map(r => r.table_name);
    console.log(`Found ${allTables.length} base tables`);

    // 2. Get columns named tenant_id and their types
    const colsResult = await pool.query(`
      SELECT table_name, column_name, data_type, udt_name
      FROM information_schema.columns
      WHERE table_schema = 'public'
        AND column_name = 'tenant_id'
      ORDER BY table_name
    `);
    const existingTenantCols = new Map();
    for (const row of colsResult.rows) {
      existingTenantCols.set(row.table_name, {
        dataType: row.data_type,
        udtName: row.udt_name,
      });
    }
    console.log(`Tables with existing tenant_id: ${existingTenantCols.size}`);
    for (const [t, info] of existingTenantCols) {
      console.log(`  ${t}: ${info.udtName}`);
    }

    // 3. Get the materialized view definition for employee_fraud_metrics
    const matViewResult = await pool.query(`
      SELECT definition FROM pg_matviews
      WHERE matviewname = 'employee_fraud_metrics'
    `);
    const matViewDef = matViewResult.rows.length > 0 ? matViewResult.rows[0].definition : null;

    // 4. Categorize tables
    const skipTables = [];
    const renameTables = [];
    const addTables = [];
    const existingUuidTables = []; // have UUID tenant_id already (just need RLS)

    for (const table of allTables) {
      if (SKIP_TABLES.has(table)) {
        skipTables.push(table);
        continue;
      }

      const colInfo = existingTenantCols.get(table);

      if (MARKETPLACE_RENAME_TABLES.has(table)) {
        renameTables.push(table);
        continue;
      }

      if (colInfo) {
        if (colInfo.udtName === 'uuid') {
          // Already has UUID tenant_id - just need RLS policy
          existingUuidTables.push(table);
        } else {
          // Has integer tenant_id but not in marketplace set - rename it too
          renameTables.push(table);
        }
        continue;
      }

      addTables.push(table);
    }

    console.log(`\nCategories:`);
    console.log(`  Skip (already done): ${skipTables.length} - ${skipTables.join(', ')}`);
    console.log(`  Existing UUID tenant_id (add RLS only): ${existingUuidTables.length}`);
    console.log(`  Rename integer tenant_id: ${renameTables.length} - ${renameTables.join(', ')}`);
    console.log(`  Add tenant_id column: ${addTables.length}`);

    // 5. Build migration SQL
    const lines = [];
    const pushLine = (s = '') => lines.push(s);

    pushLine('-- ============================================');
    pushLine('-- Migration 124: Multi-Tenancy with Row-Level Security');
    pushLine('-- Generated by: generate-tenant-migration.js');
    pushLine(`-- Generated at: ${new Date().toISOString()}`);
    pushLine(`-- Tables modified: ${addTables.length + renameTables.length + existingUuidTables.length}`);
    pushLine('-- ============================================');
    pushLine();
    pushLine('BEGIN;');
    pushLine();

    // Section 1: Update existing TeleTime UUID
    pushLine('-- ============================================');
    pushLine('-- Section 1: Update TeleTime tenant UUID');
    pushLine('-- ============================================');
    pushLine();
    pushLine(`-- Insert new tenant row with target UUID, migrate children, delete old row`);
    pushLine(`INSERT INTO tenants (id, name, slug, is_active, created_at, updated_at)`);
    pushLine(`  SELECT '${NEW_TENANT_UUID}', name, slug, is_active, created_at, updated_at`);
    pushLine(`  FROM tenants WHERE id = '${OLD_TENANT_UUID}'`);
    pushLine(`  ON CONFLICT (id) DO NOTHING;`);
    pushLine();
    pushLine(`UPDATE skulytics_import_matches SET tenant_id = '${NEW_TENANT_UUID}' WHERE tenant_id = '${OLD_TENANT_UUID}';`);
    pushLine(`UPDATE tenant_product_overrides SET tenant_id = '${NEW_TENANT_UUID}' WHERE tenant_id = '${OLD_TENANT_UUID}';`);
    pushLine(`DELETE FROM tenants WHERE id = '${OLD_TENANT_UUID}';`);
    pushLine();

    // Section 2: Rename integer tenant_id columns on marketplace tables
    if (renameTables.length > 0) {
      pushLine('-- ============================================');
      pushLine('-- Section 2: Rename integer tenant_id to marketplace_tenant_id');
      pushLine('-- ============================================');
      pushLine();
      for (const table of renameTables) {
        pushLine(`ALTER TABLE ${table} RENAME COLUMN tenant_id TO marketplace_tenant_id;`);
      }
      pushLine();
    }

    // Section 3: Add tenant_id UUID column to tables that don't have it
    pushLine('-- ============================================');
    pushLine('-- Section 3: Add tenant_id UUID column');
    pushLine('-- ============================================');
    pushLine();
    for (const table of addTables) {
      pushLine(`ALTER TABLE ${table} ADD COLUMN IF NOT EXISTS tenant_id UUID NOT NULL DEFAULT '${NEW_TENANT_UUID}' REFERENCES tenants(id);`);
    }
    // Also add UUID tenant_id to renamed marketplace tables
    for (const table of renameTables) {
      pushLine(`ALTER TABLE ${table} ADD COLUMN IF NOT EXISTS tenant_id UUID NOT NULL DEFAULT '${NEW_TENANT_UUID}' REFERENCES tenants(id);`);
    }
    pushLine();

    // Section 4: Create indexes
    pushLine('-- ============================================');
    pushLine('-- Section 4: Create indexes on tenant_id');
    pushLine('-- ============================================');
    pushLine();
    const allIndexTables = [...addTables, ...renameTables, ...existingUuidTables];
    // Also index the two skip tables that have UUID tenant_id (they might not have indexes)
    allIndexTables.push('skulytics_import_matches', 'tenant_product_overrides');
    for (const table of allIndexTables) {
      pushLine(`CREATE INDEX IF NOT EXISTS idx_${table}_tenant_id ON ${table}(tenant_id);`);
    }
    pushLine();

    // Section 5: Enable RLS on all tables (except tenants itself)
    pushLine('-- ============================================');
    pushLine('-- Section 5: Enable Row-Level Security');
    pushLine('-- ============================================');
    pushLine();
    const allRlsTables = [...addTables, ...renameTables, ...existingUuidTables, 'skulytics_import_matches', 'tenant_product_overrides'];
    for (const table of allRlsTables) {
      pushLine(`ALTER TABLE ${table} ENABLE ROW LEVEL SECURITY;`);
      pushLine(`ALTER TABLE ${table} FORCE ROW LEVEL SECURITY;`);
    }
    pushLine();

    // Section 6: Create tenant_isolation policies
    pushLine('-- ============================================');
    pushLine('-- Section 6: Create tenant_isolation policies');
    pushLine('-- ============================================');
    pushLine();
    for (const table of allRlsTables) {
      pushLine(`CREATE POLICY tenant_isolation ON ${table} USING (tenant_id = NULLIF(current_setting('app.current_tenant', true), '')::uuid) WITH CHECK (tenant_id = NULLIF(current_setting('app.current_tenant', true), '')::uuid);`);
    }
    pushLine();

    // Section 7: Recreate materialized view with tenant_id
    if (matViewDef) {
      pushLine('-- ============================================');
      pushLine('-- Section 7: Recreate employee_fraud_metrics materialized view');
      pushLine('-- ============================================');
      pushLine();
      pushLine('DROP MATERIALIZED VIEW IF EXISTS employee_fraud_metrics;');
      pushLine();
      // Inject tenant_id into the materialized view
      // The view selects from users (u) - add u.tenant_id
      let viewDef = matViewDef.trim();
      // Remove trailing semicolon if present
      if (viewDef.endsWith(';')) viewDef = viewDef.slice(0, -1);
      // Inject tenant_id into the view: add u.tenant_id to SELECT and GROUP BY
      let modifiedDef = viewDef;
      // Add u.tenant_id after "u.id AS user_id,"
      modifiedDef = modifiedDef.replace(
        /u\.id AS user_id,/i,
        'u.id AS user_id,\n    u.tenant_id,'
      );
      // Add u.tenant_id to GROUP BY
      modifiedDef = modifiedDef.replace(
        /GROUP BY u\.id,/i,
        'GROUP BY u.id, u.tenant_id,'
      );
      pushLine(`CREATE MATERIALIZED VIEW employee_fraud_metrics AS`);
      pushLine(modifiedDef + ';');
      pushLine();
      pushLine('CREATE INDEX IF NOT EXISTS idx_employee_fraud_metrics_tenant_id ON employee_fraud_metrics(tenant_id);');
      pushLine();
    }

    pushLine('COMMIT;');
    pushLine();

    // 6. Write the migration file
    const outputPath = path.join(__dirname, '..', 'migrations', '124_multi_tenancy_rls.sql');
    fs.writeFileSync(outputPath, lines.join('\n'), 'utf8');
    console.log(`\nMigration written to: ${outputPath}`);
    console.log(`Total lines: ${lines.length}`);

    await pool.end();
  } catch (err) {
    console.error('Error generating migration:', err);
    await pool.end();
    process.exit(1);
  }
}

generate();
