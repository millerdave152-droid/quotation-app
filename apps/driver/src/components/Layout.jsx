import { Outlet, NavLink } from 'react-router-dom';
import { useApp } from '../context/AppContext';

const navItems = [
  { to: '/', label: 'Route', icon: RouteIcon },
  { to: '/map', label: 'Map', icon: MapIcon },
  { to: '/profile', label: 'Profile', icon: UserIcon },
  { to: '/settings', label: 'Settings', icon: GearIcon },
];

export default function Layout() {
  const { online, pendingSyncs, toast } = useApp();

  return (
    <div className="flex h-full flex-col">
      {/* Offline banner */}
      {!online && (
        <div className="bg-amber-500 px-4 py-1 text-center text-sm font-medium text-white">
          Offline â€” changes will sync when connected
          {pendingSyncs > 0 && ` (${pendingSyncs} pending)`}
        </div>
      )}

      {/* Main content */}
      <main className="flex-1 overflow-y-auto">
        <Outlet />
      </main>

      {/* Toast */}
      {toast && (
        <div className={`fixed left-4 right-4 top-4 z-50 rounded-lg px-4 py-3 text-sm font-medium text-white shadow-lg ${
          toast.type === 'error' ? 'bg-red-600' : toast.type === 'success' ? 'bg-green-600' : 'bg-blue-600'
        }`}>
          {toast.message}
        </div>
      )}

      {/* Bottom nav */}
      <nav className="flex border-t border-slate-200 bg-white">
        {navItems.map(({ to, label, icon: Icon }) => (
          <NavLink
            key={to}
            to={to}
            end={to === '/'}
            className={({ isActive }) =>
              `flex flex-1 flex-col items-center gap-0.5 py-2 text-xs ${
                isActive ? 'text-blue-600' : 'text-slate-500'
              }`
            }
          >
            <Icon className="h-6 w-6" />
            {label}
          </NavLink>
        ))}
      </nav>
    </div>
  );
}

